template<typename T>
MarchingCubes<T>::MarchingCubes() {
    _scalarField = nullptr;
    _isoLevel = 0;
}

template<typename T>
Mesh MarchingCubes<T>::generateIsoSurface(const T *scalarField, T isoLevel, const Box3f &bounds, const Vector3i &cells) {
    _isoLevel = isoLevel;
    _cells = cells;
    _cellSize = bounds.extents().cwiseProduct(Vector3f(cells.x(), cells.y(), cells.z()).cwiseInverse());
    _origin = bounds.min;
    _scalarField = scalarField;

    int width = (_cells.x() + 1);
    int slice = width*(_cells.y() + 1);

    std::unordered_map<unsigned int, std::pair<Vector3f, unsigned int>> vertices;
    std::vector<Vector3u> triangles;

    // Generate isosurface
    for (int z = 0; z < _cells.z(); z++) {
        for (int y = 0; y < _cells.y(); y++) {
            for (int x = 0; x < _cells.x(); x++) {
                // Calculate table lookup index from those vertices which are below the isolevel
                unsigned int tableIndex = 0;
                if (_scalarField[(z  ) * slice + (y  ) * width + (x  )] < _isoLevel) tableIndex |= 1;
                if (_scalarField[(z  ) * slice + (y+1) * width + (x  )] < _isoLevel) tableIndex |= 2;
                if (_scalarField[(z  ) * slice + (y+1) * width + (x+1)] < _isoLevel) tableIndex |= 4;
                if (_scalarField[(z  ) * slice + (y  ) * width + (x+1)] < _isoLevel) tableIndex |= 8;
                if (_scalarField[(z+1) * slice + (y  ) * width + (x  )] < _isoLevel) tableIndex |= 16;
                if (_scalarField[(z+1) * slice + (y+1) * width + (x  )] < _isoLevel) tableIndex |= 32;
                if (_scalarField[(z+1) * slice + (y+1) * width + (x+1)] < _isoLevel) tableIndex |= 64;
                if (_scalarField[(z+1) * slice + (y  ) * width + (x+1)] < _isoLevel) tableIndex |= 128;

                // Skip triangulation if there is no crossing
                if (_edgeTable[tableIndex] == 0) {
                    continue;
                }

                // Now create a triangulation of the isosurface in this cell
                auto addVertex = [this, &vertices] (unsigned int x, unsigned int y, unsigned int z, unsigned int edgeNo) {
                    vertices.emplace(edgeId(x, y, z, edgeNo), std::make_pair<Vector3f, unsigned int>(calculateIntersection(x, y, z, edgeNo), 0));
                };

                if (_edgeTable[tableIndex] & 8) addVertex(x, y, z, 3);
                if (_edgeTable[tableIndex] & 1) addVertex(x, y, z, 0);
                if (_edgeTable[tableIndex] & 256) addVertex(x, y, z, 8);
                
                if (x == _cells.x() - 1) {
                    if (_edgeTable[tableIndex] & 4) addVertex(x, y, z, 2);
                    if (_edgeTable[tableIndex] & 2048) addVertex(x, y, z, 11);
                }
                if (y == _cells.y() - 1) {
                    if (_edgeTable[tableIndex] & 2) addVertex(x, y, z, 1);
                    if (_edgeTable[tableIndex] & 512) addVertex(x, y, z, 9);
                }
                if (z == _cells.z() - 1) {
                    if (_edgeTable[tableIndex] & 16) addVertex(x, y, z, 4);
                    if (_edgeTable[tableIndex] & 128) addVertex(x, y, z, 7);
                }
                if ((x == _cells.x() - 1) && (y == _cells.y() - 1)) {
                    if (_edgeTable[tableIndex] & 1024) addVertex(x, y, z, 10);
                }
                if ((x == _cells.x() - 1) && (z == _cells.z() - 1)) {
                    if (_edgeTable[tableIndex] & 64) addVertex(x, y, z, 6);
                }
                if ((y == _cells.y() - 1) && (z == _cells.z() - 1)) {
                    if (_edgeTable[tableIndex] & 32) addVertex(x, y, z, 5);
                }

                // Create triangles
                for (unsigned int i = 0; _triTable[tableIndex][i] != -1; i += 3) {
                    triangles.emplace_back(Vector3u(
                        edgeId(x, y, z, _triTable[tableIndex][i]),
                        edgeId(x, y, z, _triTable[tableIndex][i+1]),
                        edgeId(x, y, z, _triTable[tableIndex][i+2])
                    ));
                }
            }
        }
    }
    
    // Create continuous vertex indices
    unsigned int index = 0;
    for (auto &vertex : vertices) {
        vertex.second.second = index++;
    }

    // Reassign vertex indices to triangles
    for (auto &triangle : triangles) {
        for (size_t i = 0; i < 3; i++) {
            triangle[i] = vertices[triangle[i]].second;
        }
    }

    Mesh mesh;

    // Copy vertices into mesh
    mesh.vertices().resize(3, vertices.size());
    for (const auto &vertex : vertices) {
        mesh.vertices().col(vertex.second.second) = vertex.second.first;
    }

    // Copy triangles into mesh
    mesh.triangles().resize(3, triangles.size());
    index = 0;
    for (const auto &triangle : triangles) {
        mesh.triangles().col(index++) = triangle;
    }

    // Compute normals
    mesh.normals().resize(3, vertices.size());
    mesh.normals().setConstant(0.f);
    for (int i = 0; i < mesh.triangles().cols(); ++i) {
        int id0 = mesh.triangles().col(i)[0];
        int id1 = mesh.triangles().col(i)[1];
        int id2 = mesh.triangles().col(i)[2];
        Vector3f v1(mesh.vertices().col(id1) - mesh.vertices().col(id0));
        Vector3f v2(mesh.vertices().col(id2) - mesh.vertices().col(id0));
        Vector3f normal(v2.cross(v1).normalized());
        if (std::isnan(normal.x()) || std::isnan(normal.y()) || std::isnan(normal.z())) {
            continue;
        }
        mesh.normals().col(id0) += normal;
        mesh.normals().col(id1) += normal;
        mesh.normals().col(id2) += normal;
    }

    // Normalize normals
    for (int i = 0; i < mesh.normals().cols(); ++i) {
        mesh.normals().col(i).normalize();
    }

    return std::move(mesh);
}

template<typename T>
unsigned int MarchingCubes<T>::edgeId(unsigned int x, unsigned int y, unsigned int z, unsigned int edgeNo) {
    switch (edgeNo) {
    case 0:  return vertexId(x  , y  , z  )+1;
    case 1:  return vertexId(x  , y+1, z  )  ;
    case 2:  return vertexId(x+1, y  , z  )+1;
    case 3:  return vertexId(x  , y  , z  )  ;
    case 4:  return vertexId(x  , y  , z+1)+1;
    case 5:  return vertexId(x  , y+1, z+1)  ;
    case 6:  return vertexId(x+1, y  , z+1)+1;
    case 7:  return vertexId(x  , y  , z+1)  ;
    case 8:  return vertexId(x  , y  , z  )+2;
    case 9:  return vertexId(x  , y+1, z  )+2;
    case 10: return vertexId(x+1, y+1, z  )+2;
    case 11: return vertexId(x+1, y  , z  )+2;
    default: return -1;
    }
}

template<typename T>
unsigned int MarchingCubes<T>::vertexId(unsigned int x, unsigned int y, unsigned int z) {
    return 3 * (z * (_cells.x() + 1) * (_cells.y() + 1) + y * (_cells.x() + 1) + x);
}

// Calculates the intersection point of the isosurface with an edge
template<typename T>
Vector3f MarchingCubes<T>::calculateIntersection(unsigned int x, unsigned int y, unsigned int z, unsigned int edgeNo) {
    unsigned int v1x = x, v1y = y, v1z = z;
    unsigned int v2x = x, v2y = y, v2z = z;
    
    switch (edgeNo) {
    case 0:
        v2y += 1;
        break;
    case 1:
        v1y += 1;
        v2x += 1;
        v2y += 1;
        break;
    case 2:
        v1x += 1;
        v1y += 1;
        v2x += 1;
        break;
    case 3:
        v1x += 1;
        break;
    case 4:
        v1z += 1;
        v2y += 1;
        v2z += 1;
        break;
    case 5:
        v1y += 1;
        v1z += 1;
        v2x += 1;
        v2y += 1;
        v2z += 1;
        break;
    case 6:
        v1x += 1;
        v1y += 1;
        v1z += 1;
        v2x += 1;
        v2z += 1;
        break;
    case 7:
        v1x += 1;
        v1z += 1;
        v2z += 1;
        break;
    case 8:
        v2z += 1;
        break;
    case 9:
        v1y += 1;
        v2y += 1;
        v2z += 1;
        break;
    case 10:
        v1x += 1;
        v1y += 1;
        v2x += 1;
        v2y += 1;
        v2z += 1;
        break;
    case 11:
        v1x += 1;
        v2x += 1;
        v2z += 1;
        break;
    }

    Vector3f p1(v1x * _cellSize.x(), v1y * _cellSize.y(), v1z * _cellSize.z());
    Vector3f p2(v2x * _cellSize.x(), v2y * _cellSize.y(), v2z * _cellSize.z());
    unsigned int width = (_cells.x() + 1);
    unsigned int slice = width*(_cells.y() + 1);
    T val1 = _scalarField[v1z*slice + v1y*width + v1x];
    T val2 = _scalarField[v2z*slice + v2y*width + v2x];
    return _origin + interpolate(p1, p2, val1, val2);
}

// Interpolates between two grid points to produce the point at which the isosurface intersects an edge
template<typename T>
Vector3f MarchingCubes<T>::interpolate(const Vector3f &p1, const Vector3f &p2, T val1, T val2) {
    float t = float((_isoLevel - val1)) / (val2 - val1);
    return p1 + t * (p2 - p1);
}

